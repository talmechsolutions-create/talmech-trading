export function findListingStrategyRule(metal: string, product: string) {
  const haystack = `${metal} ${product}`.toLowerCase();
  return listingStrategyRules.find((rule) => rule.productKeywords.some((keyword) => haystack.includes(keyword.toLowerCase())))
    || defaultListingStrategyRule;
}
