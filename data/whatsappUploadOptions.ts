export function getProductOptions(selectedMetal: string) {
  return productOptionsByMetal[selectedMetal] || productOptionsByMetal[CUSTOM_OPTION];
}

export function getGradeOptions(selectedMetal: string) {
  return gradeOptionsByMetal[selectedMetal] || gradeOptionsByMetal[CUSTOM_OPTION];
}

export function getProductFormOptions(selectedMetal: string) {
  return selectedMetal === 'Scrap' ? scrapProductForms : commonProductForms;
}

export function finalDropdownLabel(selected: string, custom: string) {
  return selected === CUSTOM_OPTION ? custom.trim() : selected;
}
